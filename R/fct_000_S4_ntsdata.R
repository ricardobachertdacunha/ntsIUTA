setMethod("pushBlanks", "ntsData", function(object, value) {
  if (FALSE %in% unique(value %in% object@samples$group)) {
    error("Blank replicate sample groups must be one or more sample groups.")
  }
  object@samples$blank <- value
  return(object)
})

